/// Design tokens for spacing based on an 8px base unit.
abstract final class AppSpacing {
  /// 4px - Micro spacing (e.g., icon-text internal gap)
  static const double space1 = 4.0;

  /// 8px - Minimum spacing between elements
  static const double space2 = 8.0;

  /// 16px - Component internal padding / Mobile margin
  static const double space3 = 16.0;

  /// 24px - Compact form field gap / Desktop gutter
  static const double space4 = 24.0;

  /// 32px - Standard form field gap
  static const double space5 = 32.0;

  /// 40px - Mobile lateral margin / Tablet margin
  static const double space6 = 40.0;

  /// 48px - Section gap / Mobile button height
  static const double space7 = 48.0;

  /// 56px - Mobile button height
  static const double space8 = 56.0;

  /// 64px - Section vertical margin / Tablet margin
  static const double space9 = 64.0;

  /// 72px - Desktop button height / Desktop margin / Large gap
  static const double space10 = 72.0;

  /// 80px
  static const double space11 = 80.0;

  /// 96px - Title spacing / Display Large height
  static const double space12 = 96.0;
}
